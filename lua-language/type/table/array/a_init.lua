--[[
--   数组: 是以数字作为key的特殊字典
--
--   初始化方式：
--     {1, 2, 3}
--     t2 = t1； table之间的拷贝是引用拷贝，并非是值拷贝
--     t2 = {unpack(t1)}: 真正实现了两个table之间的值拷贝
--]]

-- 方式一
t1 = {10, 20, 30, 40}
print(t1[1])    -- 10

-- 方式二
t1 = {10, 20, 30, 40}
t2 = t1
t2[1] = 100
print(t1[1])    -- 100

-- 方式三
t1 = {10, 20, 30, 40}
t2 = {unpack(t1)}
t2[1] = 100
print(t1[1])    -- 10
